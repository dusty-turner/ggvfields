#' @rdname geom_stream_field
#' @format NULL
#' @usage NULL
#' @export
StatStreamField <- ggproto(
  "StatStreamField",
  Stat,
  default_aes = aes(group = after_stat(id)),

  compute_group = function(data, scales, fun, xlim, ylim, n, method, max_it = 1000, dt, L = NULL, center, normalize, ...) {

    # make grid of points on which to compute streams
    grid <- cbind(
      "x" = rep(seq(xlim[1], xlim[2], length.out = n[1]), times = n[2]),
      "y" = rep(seq(ylim[1], ylim[2], length.out = n[2]), each = n[1])
    )

    dt <- rep(dt, nrow(grid))

    if(normalize == "stream") {
      L <- (min(diff(xlim), diff(ylim)) / (max(n) - 1)) * 0.9
    } else if(normalize == "vector") {
      L <- (min(diff(xlim), diff(ylim)) / (max(n) - 1)) * 0.8
      if(center) L <- L/2 # for some reason centering a vector doubles the length in the process: fix later
    }
    else if(!normalize) L <- (min(diff(xlim), diff(ylim))) / 2

    if (normalize == "vector") {
      # Calculate dt for each grid point
      dt <- L / apply(grid, 1, function(v) sqrt(sum(f(v) ^ 2)))

      # Replace infinite dt values with 0
      dt[is.infinite(dt)] <- 0
    }

    # initialize the data frame
    df <- data.frame()

    # iterate computing stream from each point
    for (i in 1:nrow(grid)) {

      df <- rbind(
        df,
        transform(
          ode_stepper(grid[i,], fun, dt[i], 0, L, max_it, method, center),
          "id" = i
        )
      )
    }

    # return data frame of streams
    df

  }
)









ode_stepper <- function(u0, fun, dt = .0025, t0 = 0, L = 1, max_it = 1000, method = "lsoda", center = FALSE) {

  # concept: if center = TRUE, the t's are shifted 1:5 -> -2:2 or 0:6 -> -2:3
  # the implementation simply runs ode_stepper on the negative of the function
  if (center) {
    # define a few helpers
    neg_fun <- function(u) -fun(u)
    flip <- function(df) df[nrow(df):1,]

    # solve in both directions
    df_negative <- ode_stepper(u0, neg_fun, dt, t0, L/2, max_it, method, center = FALSE)
    df_negative$t <- -df_negative$t
    df_positive <- ode_stepper(u0,     fun, dt, t0, L/2, max_it, method, center = FALSE)

    # combine the datasets
    df <- rbind( flip(df_negative[-1,]), df_positive )

    # shift lengths, recompute distances, arc length, avg speed
    n_neg <- nrow(df_negative) - 1
    n_pos <- nrow(df_positive)
    n <- nrow(df)
    df$d <- c(NA, df$d[1:n_neg], df_positive$d[-1])
    df$l <- c(0, cumsum(df$d[-1]))
    df$avg_spd <- df$l[n] / (df$t[n] - df$t[1])

    # return
    return(df)
  }

  # initialize the data structure, a matrix because it drops on subsetting
  mat <- cbind(
    "t" = t0 + dt*(0:(max_it-1)),
    "x" = c(u0[1], rep(NA, max_it-1)),
    "y" = c(u0[2], rep(NA, max_it-1)),
    "d" = rep(NA, max_it),             # dist since last step
    "l" = c(    0, rep(NA, max_it-1))  # arc length = cumulative dist traveled
  )

  # "solve" with ode() step by step
  for(i in 2:max_it) {

    # get "current" t and u = (x,y)
    t <- mat[i-1,"t"]
    u <- mat[i-1,c("x","y")]

    # compute next u = (x,y). all t's were computed in advance
    mat[i,c("x","y")] <- ode(
           "y" = u,
       "times" = c(t, t + dt),
        "func" = function(t, y, parms = NULL, ...) list(fun(y)),
      "method" = method,
       "parms" = NULL
    )[2,c("x","y")]

    # break if ode fails
    if ( any( is.nan(mat[i,c("x","y")]) | is.na(mat[i,c("x","y")]) ) ) {
      df <- mat[1:(i-1),,drop=FALSE] |> matrix_to_df_with_names()
      df$avg_spd <- df$l[i-1] / df$t[i-1]
      return( df )
    }

    # compute distance traversed in that step
    mat[i,"d"] <- norm(mat[i,c("x","y")] - mat[i-1,c("x","y")])

    # update length of curve
    mat[i,"l"] <- mat[i-1,"l"] + mat[i,"d"]

    # stop if curve has exceeded max length
    if (mat[i,"l"] >= L) break

  }

  # return full rows
  row.names(mat) <- NULL
  df <- matrix_to_df_with_names(mat[1:i,])
  df$avg_spd <- df$l[i] / df$t[i]
  # if (center) center_on_point(df, u0) else df # this is for translation centering
  df

}
