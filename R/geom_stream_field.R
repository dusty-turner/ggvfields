parameterization <- function(data) {
  fx <- with(data, approxfun(t,x))
  fy <- with(data, approxfun(t,y))
  function(t) {
    n <- length(t)
    df <- cbind("x" = fx(t), "y" = fy(t)) |> matrix_to_df_with_names()
    df$d <- c(0, apply(df[2:n,] - df[1:(n-1),], 1, norm))
    df$l <- cumsum(df$d)
    df$t <- t
    df[,c("t","x","y","d","l")]
  }
}
