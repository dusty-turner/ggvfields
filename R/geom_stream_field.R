stream_length <- function(data) {

  # data is assumed to have columns t, x, y
  # t is assumed in order
  # data <- data[order(data$t),]

  # discard any columns other than x and y
  data <- data[,c("x","y")]

  # find length of path
  n <- nrow(data)
  apply(data[2:n,] - data[1:(n-1),], 1, norm) |> sum()

}
